    public void carregarArquivo(File arquivo) {
        FileReader fr = null;
        BufferedReader br = null;

        try {
            fr = new FileReader(arquivo);
            br = new BufferedReader(fr);

            String linha;
            while ((linha = br.readLine()) != null) {

                Aluno a = new Aluno();
                String arranjo[] = linha.split(",");
                if (arranjo.length >= 1) {
                    a.setMatricula(arranjo[0]);
                    a.setNome(arranjo[1]);
                    a.setTelefone(arranjo[2]);
                    a.setCPF(arranjo[3]);
                    a.setIdade(arranjo[4]);
                    a.setData(arranjo[5]);
                    aluno.add(a);

                }
            }
            TelaDi tela = new TelaDi(null, true, ca);
            tela.exibeInformacoes();

        } catch (Exception ex) {
            ex.printStackTrace();

        } finally {
            try {
                if (fr != null) {
                    fr.close();
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }

