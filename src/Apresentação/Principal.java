    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        JFileChooser selecionarArquivo = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Arquivos csv", "csv");
        selecionarArquivo.setFileFilter(filtro);

        int selecionar = selecionarArquivo.showOpenDialog(this);

        if (selecionar == JFileChooser.APPROVE_OPTION) {
            File arquivo = selecionarArquivo.getSelectedFile();
            guardarArquivo(arquivo);
        }

    }

    public void guardarArquivo(File arquivo) {
        FileWriter ficheiro = null;
        PrintWriter pw = null;
        ArrayList<Aluno> a = ca.retornarTodos();

        try {
            ficheiro = new FileWriter(arquivo);
            pw = new PrintWriter(ficheiro);

            for (int i = 0; i < a.size() && a.get(i) != null; i++) {
                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                String linha = a.get(i).getMatricula() + ", " + a.get(i).getNome() + ", " + a.get(i).getTelefone() + ", " + a.get(i).getCPF() + ", " + a.get(i).getIdade() + ", " + formato.format(a.get(i).getData());
                pw.println(linha);
            }

        } catch (Exception ex) {
            ex.printStackTrace();

        } finally {
            try {
                if (ficheiro != null) {
                    ficheiro.close();
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

