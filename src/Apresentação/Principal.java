    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        ArrayList<Aluno> aln = ca.retornarTodos();
        Aluno a = new Aluno();
        a.setNome(tdfNome.getText());
        a.setMatricula(tdfMat.getText());
        a.setTelefone(tdfTel.getText());
        a.setIdade(tdfIdade.getText());
        a.setCPF(tdfCPF.getText());
        String s = tdfData.getText();
        try {
            a.setData(s);
        } catch (ParseException ex) {
            Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (aln.isEmpty()) {
            ca.salvar(a);
            JOptionPane.showMessageDialog(null, "Aluno cadastrado com sucesso.");
            tdfNome.setText("");
            tdfMat.setText("");
            tdfTel.setText("");
            tdfIdade.setText("");
            tdfCPF.setText("");
            tdfData.setText("");
            tdfNome.requestFocus();
        } else {
            for (int i = 0; i < aln.size() && aln.get(i) != null; i++) {
                String x = tdfMat.getText();
                if (x.equals("")) {
                    JOptionPane.showMessageDialog(null, "Matricula inválida.");
                    tdfMat.requestFocus();
                    break;
                } else {
                    if (aln.size() == 1) {
                        if (aln.get(0).getMatricula().equals(x)) {
                            JOptionPane.showMessageDialog(null, "Matricula já cadastrada.");
                            tdfMat.setText("");
                            tdfMat.requestFocus();
                            break;
                        } else {
                            ca.salvar(a);
                            JOptionPane.showMessageDialog(null, "Aluno cadastrado com sucesso.");
                            tdfNome.setText("");
                            tdfMat.setText("");
                            tdfTel.setText("");
                            tdfIdade.setText("");
                            tdfCPF.setText("");
                            tdfData.setText("");
                            tdfNome.requestFocus();
                            break;
                        }
                    }
                    if (aln.size() > 1) {
                        if (aln.get(0).getMatricula().equals(x)) {
                            JOptionPane.showMessageDialog(null, "Matricula já cadastrada.");
                            tdfMat.setText("");
                            tdfMat.requestFocus();
                            break;
                        } else {
                            if (ca.matriculaD(x, ca)) {
                                JOptionPane.showMessageDialog(null, "Matricula já cadastrada.");
                                tdfMat.setText("");
                                tdfMat.requestFocus();
                                break;
                            } else {
                                ca.salvar(a);
                                JOptionPane.showMessageDialog(null, "Aluno cadastrado com sucesso.");
                                tdfNome.setText("");
                                tdfMat.setText("");
                                tdfTel.setText("");
                                tdfIdade.setText("");
                                tdfCPF.setText("");
                                tdfData.setText("");
                                tdfNome.requestFocus();
                                break;
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

